//math operations

function add(a, b) {
    return a + b;
}

console.log(add(10, 45));

function subtract(a, b) {
    return a - b;
}

console.log(subtract(43, 8));

function multiply(a, b) {
    return a * b;
}

console.log(multiply(2, 9));

function divide(a, b) {
    return a / b;
}

console.log(divide(48, 2));